package ArbolBusqueda;

public abstract class Comparador {
    
    public abstract boolean igualQue(Object q);
    
    public abstract boolean mayorQue(Object q);
    
    public abstract boolean mayorIgualQue(Object q);
    
    public abstract boolean menorQue(Object q);
   
    public abstract boolean menorIgualQue(Object q);
    
}

